      callName: 'exercise',
      apiUrl:
          'https://wger.de/api/v2/exerciseinfo/?api-id=5f6bddf98a310d435d4987deabe977c02ae6ba26?format=api',
      callType: ApiCallType.GET,
      headers: {},
